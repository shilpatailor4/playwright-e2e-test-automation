// Arithmetic operators

let a = 20;
let b = 30;

let add = a + b;
console.log(add);
let sub = a - b;
console.log(sub);
let mul = a * b;
console.log(mul);
let div = a / b;
console.log(div);

// assignment operators

let x = 10;
console.log((x += 5)); // x = +5
console.log((x -= 5)); // x = -5
console.log((x *= 5)); // x = x*5

// comparison operators (==) && (===)

// logical operators (&&) (|) (!)s

console.log(10 < 20 && 10 < 5);

console.log(10 < 20 || 10 < 5);
