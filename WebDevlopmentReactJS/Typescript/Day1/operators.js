// Arithmetic operators
var a = 20;
var b = 30;
var add = a + b;
console.log(add);
var sub = a - b;
console.log(sub);
var mul = a * b;
console.log(mul);
var div = a / b;
console.log(div);
// assignment operators
var x = 10;
console.log((x += 5)); // x = +5
console.log((x -= 5)); // x = -5
console.log((x *= 5)); // x = x*5
// comparison operators (==) && (===)
// logical operators (&&) (|) (!)s
console.log(10 < 20 && 10 < 5);
console.log(10 < 20 || 10 < 5);
