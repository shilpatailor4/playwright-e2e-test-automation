//Print all even numbers from 0 to 100.
for(let i=0; i<=10; i++){
    if(i % 2 === 0){
        //Even num
       console.log("Even number =", i);
    }   
}

//Print all odd numbers from 0 to 100.
for(let i=0; i<=10; i++){
    if(i % 2 !== 0){
        //Odd num
       console.log("Odd number =", i);
    }   
}